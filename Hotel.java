package Hotel_Reservation.src;

public class Hotel {
    private int roomInventory;

    public void roomInfo(int roomInventory){
        this.roomInventory=roomInventory;

    }
    void reserveRoom(int roomInventory){
        this.roomInventory=roomInventory;

    }
    void cancelRoom(int roomInventory){
        this.roomInventory=roomInventory;
    }

}
